function show(){
    const sidebar = document.querySelector('.sidebar')
    const side = document.querySelector('.hide')
    sidebar.style.display = 'flex'
    // side.style.display = 'none'
}
function hide(){
    const sidebar = document.querySelector('.sidebar')
    const side = document.querySelector('.hide')
    sidebar.style.display = 'none'
    // side.style.display = 'flex'
}